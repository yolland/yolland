public class variabel_panjang {
        public static void main(String[] args) {
            System.out.printf("Persegi mengikuti nilai dari variabel panjang");
            int akhir=110;

            for (int i=1;i<=akhir;i++){
                System.out.print(i);
                System.out.print(" ");
                if(i%11==0){
                    System.out.println("\n");
                }

            }
        }
    }



